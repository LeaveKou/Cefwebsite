<h1 class="title">Historia Del CEF</h1>



<div class="texto">

 <img alt="Educación" src="imagenes/historia1.jpg">
 <p><b>Nuestra Misión:</b> Desde hace 11 años, el CEF 152 tiene como misión <b>continuar promoviendo y posibilitando la inclusión de todos los sectores sociales</b>, en especial de los sectores socioeconómicos menos favorecidos, procurando atender a su educación permanente con propuestas de actividades corporales y motrices, recreacionales y saludables, en sus distintas formas expresivas, deportivas y lúdicas, en contacto con diversos medios urbanos y naturales, que aseguren experiencias valiosas para todos contando además con la posibilidad de participar en la producción, gestión y evaluación de proyectos que realizamos. </p>
</div>

<div class="texto">

<img alt="Juan Carlos Zabala" src="imagenes/historia2.jpg">

 <p><b>Historia:</b> El Centro de Educación Física  N° 152 “Juan Carlos Zabala” (creado el 27 de agosto de 2007 por convenio entre la Municipalidad de Avellaneda y la Dirección General de Cultura y Educación) se establece en el predio donado por la fábrica Lever Hnos en la calle M. Estévez 918/920 de la Localidad de Dock Sud, inmerso en el Barrio “Villa Tranquila”.</p>
</div>

<div class="texto">

<img alt="Inicios CEF" src="imagenes/historia3.jpg">

 <p> Con el  correr de los años, el CEF muda su Sede al edificio de la EES N° 27 en la Av Roca 325, a unas pocas cuadras de sus inicios.  Esta  zona centro-este de la Ciudad de Avellaneda se encuentra delimitada por el Riachuelo, la calle M. Estévez,  la Avda. Roca, La Calle 12 de Octubre y el Barrio Dock Sud.</p></div>

<div class="texto2">
<p>Desde la mencionada sede, el Club Juventud de  Roca, la Unidad Sanitaria municipal N°4 y distintas instituciones y organizaciones gubernamentales, Centros de Jubilados, Clubes de Barrio, ONGs, etc, del barrio de Villa Tranquila comienzan a formar parte del polo socio educativo con la  EES 27, EP 11.</p>
<p>Desde el 2015, establecidos en el nuevo espacio, no contando con edificio propio y la llegada de nuevos integrantes al Equipo de Conducción Institucional, el CEF extiende sus servicios más allá del propio barrio, llevando sus propuestas a todo el distrito de Avellaneda, atendiendo a demandas de la comunidad en relación a prácticas corporales y motrices con apoyaturas, extensiones, articulaciones, proyectos y distintas actividades.</p>
<p>El CEF es una institución que con solo 30 horas cátedras hoy atiende a una  matrícula en expansión de más de 500 alumnos, sin contar con el alcance a todos los destinatarios primarios y secundarios de los proyectos, eventos, encuentros y  actividades que desarrollamos a lo largo de todo el año.</p></div>

<h1 class="title">Juan Carlos Zabala</h1>

<div class="carlos_zabala">

<div class="zabala">
<img src="imagenes/zabala.png" alt="Carlos Zabala">
<p>Juan Carlos Zabala (apodado "El Ñandú criollo") nació en Rosario, un 11 de octubre de 1911, y falleció en Buenos Aires el dia 24 de enero de 1983.</p>
<p>Fue un atleta argentino, <b>campeón olímpico de maratón en los Juegos Olímpicos de Los Ángeles 1932</b>.
Practicó además al fútbol, baloncesto y natación.</p>
</div>



