<p><a name="gentecef">Sus mejores presentaciones las desarrolló en Los Ángeles disputando la maratón en los Juegos Olímpicos en la jornada del 7 de agosto de 1932. A 4 km del final, se escapó para terminar en solitario aventajando en 20 segundos a Samuel Ferris de Gran Bretaña, consiguiendo el oro olímpico y haciendo ondear la bandera Argentina en lo más alto del mástil.</a></p>

